    p.mb-4 Una campaña publicitaria es un plan de comunicación, o parte de este, que se planea a corto o mediano plazo; actualmente, y debido a múltiples factores como el crecimiento del mercado, la llegada permanente de competidores, la globalización y la velocidad con la que llega la información, se planean a menor tiempo o con más frecuencia. Hoy en día, es menos usual ver campañas con permanencia mayor a tres meses en los medios, adicionalmente, las redes sociales permiten realizarlas con mucha más facilidad que antes, con inversiones menores y producciones más sencillas y personalizadas. No obstante, cualquier campaña, sin importar el producto o servicio que esté publicitando, sigue siendo un proceso de comunicación entre un anunciante y un consumidor, en el que intervienen además otros actores, como la agencia de publicidad, el freelancer o grupo creativo. 
    p.mb-5 El desarrollo de una campaña publicitaria implica asuntos explorados anteriormente: la investigación, el brief y la identidad corporativa; dichos elementos, son la base para el proceso de comunicación y trazan la ruta para crear una conversación eficaz con el cliente. Es importante aclarar que existen muchos modelos de campaña publicitaria, prácticamente cada agencia de publicidad tiene uno propio y diferente; sin embargo, todos se acercan a un proceso muy similar con elementos estándar que se verán a continuación. Cabe aclarar, que algunos de los que se mencionan, se profundizarán en otros materiales, por lo que los conceptos aquí expuestos son básicos.
  
